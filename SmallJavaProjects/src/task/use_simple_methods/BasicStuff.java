package task.use_simple_methods;

public class BasicStuff {
	
	public static void whileLoop() {
		
		int a = 3;
		while (a < 6) {
		System.out.println("Test");
		a++;
		}
	}
	
	public static void forLoop() {
		
		int a = 7;
		int i;
		for (i = 1; i <= 5; i++) {
		
		System.out.println(a + " X "  + i + " = " + a*i);
		
		}
	}
}
